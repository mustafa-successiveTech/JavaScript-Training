/* 8. Write a program to dispaly number from 1-20 using all types of loops */

// 1. Using for loop
console.log("Using for loop:");
for (let i = 1; i <= 20; i++) {
    console.log(i);
}

// 2. Using while loop
console.log("\nUsing while loop:");
let j = 1;
while (j <= 20) {
    console.log(j);
    j++;
}

// 3. Using do...while loop
console.log("\nUsing do...while loop:");
let k = 1;
do {
    console.log(k);
    k++;
} while (k <= 20);